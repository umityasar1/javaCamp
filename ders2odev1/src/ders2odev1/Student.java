package ders2odev1;

public class Student {

	int id ;
	String firstName;
	String lastName;
	String details;

	public Student() {
		
		
		
		
	}
	
	public Student(int id, String firstName, String lastName, String details) {
		
		this();
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.details = details;

			
	}
}
