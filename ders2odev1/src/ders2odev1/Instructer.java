package ders2odev1;

public class Instructer {

	int id;
	String firstName;
	String lastName;
	String details;

	public Instructer() {

	}

	public Instructer(int id, String firstName, String lastName, String details) {
		this();
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.details = details;

	}

}
